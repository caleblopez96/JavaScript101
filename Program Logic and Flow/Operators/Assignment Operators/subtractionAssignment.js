// The subtraction assignment(-=) operator performs subtraction on the two operands and assigns the result to the left operand.

// SYNTAX: a = a - 5 
// a -= 5

// EXAMPLE:
let a = 5
let b = 1
let c = a -= b
console.log(c) // 4


