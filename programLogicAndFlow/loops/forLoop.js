// allows you to repeatedly execute a block of code a specified number of times or based on a condition

// useful when you want to perform a task multiple times with incremental or changing values.

// SYNTAX:
for (initialization; condition; update) {
    // Code to be executed in each iteration
    }

// EXAMPLE:
for (var i = 1; i <= 5; i++) {
    console.log(i); // 1 2 3 4 5
}
