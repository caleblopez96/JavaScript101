// use the .add method to add a classlist to an HTML element 

// SYNTAX:
// element.classList.add("className")

// PARAMETERS:
// className - name of the class you want to add to the elements class list. 

// EXAMPLE: 
const dropdown = document.querySelector('.dropdown');
dropdown.classList.add('active');


