// String constructor creates string objects

// SYNTAX:
// new String(string)
// String(string)

// PARAMETERS:
// string - anything to be converted to a string

// RETURNS:
// String("string"); --> "string"
// new String("string") --> String {"string"}

// EXAMPLE:

let word1 = String("Hello!"); // 'Hello!'
let word2 = new String("Hello!"); // String {"Hello!"}

