// .trimEnd() removes whitespace at the end of a string

// SYNTAX:
// .trimEnd()

// PARAMETERS:
// none

// RETURNS
// a new string with the white space at the end removed

// EXAMPLE:
"Hello, World!   ".trimEnd(); // "Hello, World!"

