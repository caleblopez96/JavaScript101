// .substring() returns the part of the string from the start index up to and excluding the end index, or to the end of the string if no end index is supplied.

// SYNTAX:
// .substring(indexStart)
// .substring(indexStart, indexEnd)

// PARAMETERS:
// indexStart - the index of the first character to include in the substring
// indexEnd - the index of the last character to include in the substring

// RETURNS:
// a new string with the specified substring

// EXAMPLE:
"JavaScript".substring(0, 4); // 'Java'

// REFERENCE:
// MDN:
// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/String/substring
