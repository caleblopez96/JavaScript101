// .toUpperCase() converts all characters of a string to lowercase

// SYNTAX:
// .toUpperCase()

// PARAMETERS:
// none

// RETURNS:
// a new string converted to upper case

"javascript".toUpperCase(); // 'JAVASCRIPT'

