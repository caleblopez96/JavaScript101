// .reverse() reverses an array

// SYNTAX:
// .reverse()

// PARAMETERS:
// None

// RETURNS:
// the original array reversed

// MUTATION:
// MUTATES original array

// EXAMPLE:
const oneThroughFive = [1, 2, 3, 4, 5];
fiveThroughOne = oneThroughFive.reverse();
console.log(fiveThroughOne); // [5, 4, 3, 2, 1]
