// .indexOf() finds the index of a specified element

// SYNTAX:
// .indexOf(searchElement)
// .indexOf(searchElement, fromIndex)

// PARAMETERS:
// searchElement - element you are searching for
// fromIndex - index to start search from

// RETURNS:
// index of searchElement in the array; -1 if not found

// MUTATION:
// does NOT mutate original array.

// EXAMPLE:
const schoolSubjects = ["Math", "Science", "Social Studies"];
schoolSubjects.indexOf("Math"); // 0
schoolSubjects.indexOf("Language Arts"); // -1

