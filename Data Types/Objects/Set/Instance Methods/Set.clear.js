// The clear() method of Set instances removes all elements from this set.

// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Set/clear

// SYNTAX:
// Set.clear()

const mySet2 = new Set()
mySet2.add(1)
mySet2.add(2)
console.log(mySet2) // Set {}
mySet2.clear()
console.log(mySet2) // Set {}