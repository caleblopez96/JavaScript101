// array constructor creates array objects

// SYNTAX:
// new Array()
// new Array(element1, element3 /* ... */ elementN)
// OR
// Array()
// Array(element1, element3 /* ... */ elementN)

console.log(new Array(1, 2, 3)) // [1, 2, 3]
console.log(new Array(1, 2, 3)) // [1, 2, 3]

console.log(Array(1, 2, 3)) // [1, 2, 3]
console.log(Array(1, 2, 3)) // [1, 2, 3]