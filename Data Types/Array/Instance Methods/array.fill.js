// fill() changes all elements with a range 

// SYNTAX:
// fill(value)
// fill(value, start)
// fill(value, start, end)

// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Array/fill

// EXAMPLE:
const example = [1, 2, 3, 'a', 5];
example.fill(4, 3, 4); // [1, 2, 3, 4, 5]
