// Find Unique Numbers
// Given an array of numbers where every number appears twice except for one, write a function to find the unique number.

// Example Input: uniqueNumber([2, 3, 5, 3, 2])
// Example Output: 5

const givenArray = [2, 3, 5, 3, 2]

function findUniqueNumbers(array) {
  return array.forEach(element => {
    return element
  });
}

console.log(findUniqueNumbers(givenArray))