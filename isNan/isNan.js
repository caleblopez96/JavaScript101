// isNaN() checks to see if a value is NOT a number
// true if not a number
// false if a number

// SYNTAX:
// isNan(value)

// PARAMETERS:
// value - value to check if value is not a number

// NaN values are always considered unique so:
// EXAMPLE:
console.log(isNaN("nope")); // true
console.log(isNaN(5)); // false
console.log(isNaN("15")); // false

