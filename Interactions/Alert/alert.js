// Alert shows a message on the screen and waits 
// for the user to press OK before continuing.

// SYNTAX:
// alert()
// alert(message)

// https://developer.mozilla.org/en-US/docs/Web/API/Window/alert

// EXAMPLE:
let legalAge = 21;
let alertMessage = `You must be ${legalAge} to enter!`;

alert(alertMessage); // You must be 21 to answer