// The Math.sign() static method returns 1 or -1,
// indicating the sign of the number passed as 
// argument. If the input is 0 or -0, it will be 
// returned as-is.

// SYNTAX:
// Math.sign(x)

// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Math/sign

// EXAMPLE:
console.log(Math.sign(3));
// Expected output: 1

console.log(Math.sign(-3));
// Expected output: -1

console.log(Math.sign(0));
// Expected output: 0

console.log(Math.sign('-3'));
// Expected output: -1
