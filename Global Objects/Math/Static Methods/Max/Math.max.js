// Math.max() returns largest number

// SYNTAX:
// Math.max()
// Math.max(value1)
// Math.max(value1, value2)
// Math.max(value1, value2, /*...*/ valueN)

// PARAMETERS:
// value - zero or more numbers to pick the max from

// RETURNS:
// largest number

// EXAMPLE:
console.log(Math.max(1, 3, 2)); // 3

// EXAMPLE: with an array
const array1 = [1, 3, 2];
console.log(Math.max(...array1)); // 3

