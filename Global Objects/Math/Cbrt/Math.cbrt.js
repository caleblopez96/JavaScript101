// The Math.cbrt() static method returns the 
// cube root of a number.

// SYNTAX:
// Math.cbrt(x)

// EXAMPLE:
console.log(Math.cbrt(-1));
// Expected output: -1

console.log(Math.cbrt(1));
// Expected output: 1

console.log(Math.cbrt(Infinity));
// Expected output: Infinity

console.log(Math.cbrt(64));
// Expected output: 4