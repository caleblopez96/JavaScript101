// .setItem() allows you to set a key-value pair string at the localStorage

// SYNTAX:
// localStorage.setItem("key", "value")

// PARAMETERS:
// key - key to add to localStorage
// value - value to add to localStorage

// RETURNS:
// undefined

// EXAMPLE:
localStorage.setItem("myDog", "B's"); // Storage {myDog: "B's"}

// REFERENCE:
// https://developer.mozilla.org/en-US/docs/Web/API/Window/localStorage
