// .getItem() gets current item in local storage

// SYNTAX:
// localStorage.getItem(key);

// PARAMETERS:
// key - key of item in local storage you want to retrieve

// RETURNS:
// the item in local storage or null if not found

// EXAMPLE:
// set item
localStorage.setItem("myDog", "B's");

// get the item
localStorage.getItem("myDog"); // B's

// REFERENCE:
// https://developer.mozilla.org/en-US/docs/Web/API/Window/localStorage
