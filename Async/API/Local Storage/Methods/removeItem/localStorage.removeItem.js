// .removeItem() removes an item from local storage

// STYNTAX:
// localStorage.removeItem(key)

// PARAMETERS:
// key - value to remove from localStorage

// RETURNS:
// undefined

// EXAMPLE:
// set item
localStorage.setItem("myDog", "B's");

// remove item
localStorage.removeItem("myDog");

// REFERENCE:
// MDN:
// https://developer.mozilla.org/en-US/docs/Web/API/Window/localStorage
