// for loops in JavaScript are used for iterating over sequences of data,
// such as arrays and strings, or for performing a repetitive task a specific number of times.

// EXAMPLE:
// No loop:
text += cars [0] + "<br>";
text += cars [1] + "<br>";
text += cars [2] + "<br>";

// for loop
for (let i = 0; i < cars.length; i++) {
    text += cars[i] + "<br>";
}

