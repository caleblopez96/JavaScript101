// Use for...of loops to iterate thorugh the values of an iterable object:
// loops over: arrays, strings, maps, nodelists

// EXAMPLE: 

// arrays
const myArray = [1, 2, 3, 4];

for (const element of myArray) {
    console.log(element);
}

// strings
const string = "Hello World";

for (letter of string) {
    console.log(letter);
}